create or replace
PACKAGE BODY SCHOOL_API AS

  PROCEDURE GET_NAME_ADDRESS(
  TBL_NAME IN CHAR, 
  INST_ID IN NUMBER, 
  FI_NAME OUT CHAR,
  LA_NAME OUT CHAR,
  STRT OUT CHAR,
  CTY OUT CHAR,
  STTE OUT CHAR,
  ZPCD OUT NUMBER) IS
  BEGIN
    SELECT FNAME,LNAME,STREET,CITY,STATE,ZIP_CODE INTO FI_NAME,LA_NAME,STRT,CTY,STTE,ZPCD FROM INSTRUCTOR WHERE IN_ID=INST_ID;
