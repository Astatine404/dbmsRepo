  FUNCTION INSTRUCTOR_STATUS(INST_ID NUMBER) RETURN VARCHAR IS
  SEC NUMBER;
  RES VARCHAR(50);
  BEGIN
    SELECT SECTION_NOS INTO SEC FROM INSTRUCTOR WHERE IN_ID=INST_ID;
    IF SEC>=3 THEN
      RES:= q'!TEACHER NEEDS VACATION!';
    ELSE
      RES:= q'!TEACHERS TEACHES !' || SEC;
    END IF;
    RETURN RES;
  END INSTRUCTOR_STATUS;

END SCHOOL_API;

